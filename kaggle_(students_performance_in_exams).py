"""## Q 07. Correlation Heatmap for Standard Lunch Students Theory

External factors such as nutrition may affect academic performance.

Explanation

Data is filtered for students with standard lunch.

Correlation heatmap shows stronger score relationships.

Indicates better consistency in performance for students with proper nutrition.
"""

standard_lunch_corr = df[df["lunch"] == "standard"][
    ["math score","reading score","writing score"]
].corr()

plt.figure(figsize=(6,4))
