"""## Q 06. Heatmap of Correlation Matrix Theory

A heatmap represents correlation values using colors.

Explanation

Darker colors represent stronger relationships.

Makes correlation interpretation easier and faster.

Confirms strong interdependence between subjects.
"""

# Heatmap
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# Load dataset
df = pd.read_csv("StudentsPerformance.csv")

# Correlation matrix
corr = df[["math score", "reading score", "writing score"]].corr()

# Heatmap
plt.figure(figsize=(7, 5))
sns.heatmap(corr, annot=True, cmap="coolwarm", linewidths=0.5)
plt.title("Correlation Heatmap of Exam Scores")
plt.show()

corr = df[["math score","reading score","writing score"]].corr()

